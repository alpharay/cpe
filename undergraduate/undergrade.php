		<strong>Curriculum Overview</strong><BR><BR>
		The Department runs an intensive four-year program that culminates in a Bachelor 
                of Science (BSc) degree in Computer Engineering with strong liberal arts background
                The curriculum for the bachelor program broadly encompasses studies in hardware, 
                software, computer systems and communication, which are essential in the design, analysis
                , development, and application of computer and digital systems. The range of courses offered
                equip students with knowledge and skill adequate to enable them specialize in the various 
                areas of computer engineering at the department; software systems, communication networks,
                hardware systems, and computer systems among others.  In addition to the technical courses
                offered, students are also offered the opportunity to apply their knowledge to practical 
                problems of industry through internship programs. The Department maintains a minimum requirement
                of two internships (industrial practice) before graduation to strengthen the practical knowledge
                and industrial exposure of students and to make them suitable for the industry and research. 
                Seminar series are held at least twice a semester to offer students the opportunity to interact
                with industrial experts.<br/><br/>
                
