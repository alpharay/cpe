              <strong><u>PhD Programme</u></strong><BR><BR>
			To qualify for admission to pursue a Doctor of Philosophy (PhD) in
                        Computer Engineering, the candidate must meet the general admission 
                        requirements of the University of Ghana and specific admission criteria 
                        from the Department. Each application will be considered individually and 
                        the complete profile of strengths and prospects for successful completion 
                        of the programme will be evaluated. 
<br/><br/>To be admitted into the programme, a candidate must meet the following conditions: 
                            <ul style="padding-left:50px; ">
                            <li/>(a) Candidate must have an MPhil degree in Computer Engineering, Computer Science, Electrical and Electronics Engineering, Telecommunications Engineering or any other closely related discipline from a recognized University. 
                            <li/>(b) Candidate must submit a 7-page (max 10 pages) synopsis outlining the area of research interest for assessment. The synopsis must have a title, statement of the problem, objectives of the research, significance of the research to the advancement of knowledge or society, justification for the research, and minimal literature stating the method the candidate intends to use to solve the proposed problem. The font size must be 12 and new Times Roman. 
                            <li/>(c) Candidate must obtain a pass in a qualifying examination/interview to be scheduled by the Department.
                            <li/>(d) Candidates from other Engineering disciplines or from Science/Mathematics disciplines may be considered for admission on a case-by-case basis. However, for such candidates, taking of additional or prescribed undergraduate courses in Computer Engineering may be required.
                           
                            </ul> 
<div style="color:blue;">Specific cases for admission into the PhD programme: </div>
                            <ul style="padding-left:50px; ">
                            <li/>(a) Candidates with one (1) year Master's degree (MSc/MEng) with an average performance of B+ or GPA of 3.5 and above or equivalent may be considered for admission into the PhD programme. Such candidates may be required to enroll directly into the PhD programme and at the end of the first year of the programme, candidates with an average performance of less than B+ will be advised to seek for MPhil degree. 
                            <li/>(b) Candidates who have registered for MPhil in the Department and have achieved an average GPA of 3.5 or better after the first year and have expressed interest may also be considered for admission into the PhD programme. This will be done on a case-by-case basis. 
                            <li/>(c) Candidates from other Engineering disciplines or from the Science/Mathematics disciplines may be considered for admission on a case-by-case basis. However, for such candidates, additional undergraduate or prescribed courses in Computer Engineering may be required. 
                        <br/>                            
                            </ul>

